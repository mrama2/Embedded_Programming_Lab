	AREA mydata, DATA
FILL_VAL EQU    0xAAAAAAAA		
SOURCE  EQU 	0x10000100
DEST    EQU     0x10000300
COUNT   EQU     100			
		
	AREA MEM_OP, CODE, READONLY, ALIGN=2

	EXPORT __main
ENTRY
__main
    
		LDR R0, =SOURCE
		LDR R1, =FILL_VAL
		LDR R2, =COUNT
		LDR R3,=DEST
		;FILL THE MEMORY WITH 0xAA
STO		STR R1,[R0], #4
		SUBS R2,R2,#4
		BNE STO
	
		;RESTORE THE REGISTER VALUES
		LDR R0, =SOURCE
		LDR R2, =COUNT
	
		;MOVE THE SOURCE TO DEST
STO1	LDR R4,[R0],#4
        STR R4, [R3],#4
		SUBS R2,R2,#4
		BNE STO1
	
L   	B L
		END
 